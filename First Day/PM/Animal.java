package PM;

	class animal {
	  public void animalSound() {
	    System.out.println("The animal makes a sound");
	  }
	}

	class Cat extends animal {
	  public void animalSound() {
	    System.out.println("The cat says: meow meow");
	  }
	}

	class Dog extends animal {
	  public void animalSound() {
	    System.out.println("The dog says: gau gau");
	  }
	}

	


